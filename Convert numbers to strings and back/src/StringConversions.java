
class StringConversions {
	public static void main(String args[]){
		int num=19648;
		
		System.out.println(num+" in binar format: "+Integer.toBinaryString(num));
		
		System.out.println(num+" in octal format: "+Integer.toOctalString(num));
		
		System.out.println(num+" in hex format: "+Integer.toHexString(num));
	}

}
